            <script>
                const slider = document.getElementById('slider');
                const slides = document.querySelectorAll('.slide');
                const dotsContainer = document.getElementById('dots');
                let currentIndex = 0;
                const totalSlides = slides.length;
                let autoSlideInterval;
                let visibleSlides = 3;

                // Determine visible slides based on screen size
                function updateVisibleSlides() {
                    if (window.innerWidth <= 480) {
                        visibleSlides = 1;
                    } else if (window.innerWidth <= 768) {
                        visibleSlides = 2;
                    } else {
                        visibleSlides = 3;
                    }
                    updateDots();
                    updateSlider();
                }

                // Create dots based on visible slides
                function updateDots() {
                    dotsContainer.innerHTML = '';
                    const dotCount = totalSlides - visibleSlides + 1;
                    for (let i = 0; i < dotCount; i++) {
                        const dot = document.createElement('span');
                        dot.classList.add('dot');
                        dot.addEventListener('click', () => goToSlide(i));
                        dotsContainer.appendChild(dot);
                    }
                }

                const dots = document.querySelectorAll('.dot');
                if (dots.length > 0) dots[currentIndex].classList.add('active');

                function updateSlider() {
                    const slideWidth = 100 / visibleSlides;
                    slider.style.transform = `translateX(-${currentIndex * slideWidth}%)`;
                    const dots = document.querySelectorAll('.dot');
                    dots.forEach(dot => dot.classList.remove('active'));
                    if (dots[currentIndex]) dots[currentIndex].classList.add('active');
                }

                function nextSlide() {
                    const maxIndex = totalSlides - visibleSlides;
                    currentIndex++;
                    if (currentIndex > maxIndex) {
                        currentIndex = 0;
                    }
                    updateSlider();

                }

                function prevSlide() {
                    currentIndex--;
                    if (currentIndex < 0) {
                        currentIndex = totalSlides - visibleSlides;
                    }
                    updateSlider();
                }

                function goToSlide(index) {
                    currentIndex = index;
                    updateSlider();
                }

                // Auto-slide every 3 seconds
                function startAutoSlide() {
                    autoSlideInterval = setInterval(nextSlide, 3000);
                }

                // Stop auto-slide on arrow or dot click
                function stopAutoSlide() {
                    clearInterval(autoSlideInterval);
                }

                // Initial setup
                updateVisibleSlides();
                window.addEventListener('resize', updateVisibleSlides);

                document.querySelector('.arrow.left').addEventListener('click', stopAutoSlide);
                document.querySelector('.arrow.right').addEventListener('click', stopAutoSlide);

                // Start auto-slide on page load
                startAutoSlide();
            </script>
