        // Reveal on scroll
        const obs = new IntersectionObserver(es => {
            es.forEach(e => {
                if (e.isIntersecting) e.target.classList.add('vis');
            });
        }, {
            threshold: .08,
            rootMargin: '0px 0px -40px 0px'
        });
        document.querySelectorAll('.reveal,.reveal-left,.reveal-right').forEach(el => obs.observe(el));

        // How it works tabs
        function switchTab(id) {
            document.querySelectorAll('.hw-tab').forEach(t => t.classList.remove('active'));
            event.currentTarget.classList.add('active');
            document.querySelectorAll('.hw-panel').forEach(p => p.classList.toggle('active', p.id === 'steps-' + id));
            document.querySelectorAll('.hw-viz-inner').forEach(v => v.classList.toggle('active', v.id === 'viz-' + id));
        }

        // FAQ accordion
        function toggleF(el) {
            const item = el.closest('.fitem'),
                open = item.classList.contains('open');
            document.querySelectorAll('.fitem.open').forEach(i => i.classList.remove('open'));
            if (!open) item.classList.add('open');
        }
    </script>

