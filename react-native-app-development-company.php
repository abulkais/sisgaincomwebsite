            <script>
                (function() {
                    const AUTOPLAY_MS = 5000;

                    function initSlider(container) {
                        const track = container.querySelector('.slider-track');
                        const prevBtn = container.querySelector('.ctrl-btn.prev');
                        const nextBtn = container.querySelector('.ctrl-btn.next');
                        let originals = Array.from(track.children);
                        let slideCount = originals.length;
                        let slidesToShow = computeSlidesToShow();
                        let index = 0;
                        let timer = null;
                        let transitioning = false;

                        function computeSlidesToShow() {
                            const w = window.innerWidth;
                            if (w < 768) return 1;
                            if (w < 992) return 2;
                            return 3;
                        }

                        function getSlideWidth() {
                            const anySlide = track.querySelector('.slide');
                            return anySlide ? anySlide.getBoundingClientRect().width : 0;
                        }

                        function buildClones() {
                            // remove all children and re-append originals then clones
                            const originalSlides = Array.from(track.children).filter(n => !n.classList.contains('clone'));
                            track.innerHTML = '';
                            originalSlides.forEach(n => {
                                n.classList.remove('clone');
                                track.appendChild(n);
                            });

                            const slides = Array.from(track.children);
                            for (let i = 0; i < slidesToShow; i++) {
                                const cloneStart = slides[slides.length - 1 - i].cloneNode(true);
                                cloneStart.classList.add('clone');
                                track.insertBefore(cloneStart, track.firstChild);
                                const cloneEnd = slides[i].cloneNode(true);
                                cloneEnd.classList.add('clone');
                                track.appendChild(cloneEnd);
                            }
                        }

                        function setSizes() {
                            slidesToShow = computeSlidesToShow();
                            const frameWidth = container.clientWidth;
                            const slideWidth = frameWidth / slidesToShow;
                            Array.from(track.children).forEach(s => {
                                s.style.minWidth = slideWidth + 'px';
                                s.style.maxWidth = slideWidth + 'px';
                            });
                        }

                        function updatePosition(animated = true) {
                            const slideW = getSlideWidth();
                            const offset = -(slidesToShow + index) * slideW;
                            if (!animated) {
                                track.style.transition = 'none';
                                track.style.transform = `translateX(${offset}px)`;
                                // force reflow
                                track.getBoundingClientRect();
                                track.style.transition = 'transform 0.6s cubic-bezier(.22,.9,.3,1)';
                            } else {
                                track.style.transform = `translateX(${offset}px)`;
                            }
                        }

                        function goTo(newIndex, animate = true) {
                            if (transitioning) return;
                            transitioning = true;
                            index = newIndex;
                            updatePosition(animate);
                            if (animate) {
                                const onEnd = () => {
                                    track.removeEventListener('transitionend', onEnd);
                                    if (index >= slideCount) {
                                        index = index - slideCount;
                                        updatePosition(false);
                                    } else if (index < 0) {
                                        index = index + slideCount;
                                        updatePosition(false);
                                    }
                                    transitioning = false;
                                };
                                track.addEventListener('transitionend', onEnd);
                            } else {
                                transitioning = false;
                            }
                        }

                        function next() {
                            goTo(index + 1, true);
                        }

                        function prev() {
                            goTo(index - 1, true);
                        }

                        function startAutoplay() {
                            stopAutoplay();
                            timer = setInterval(next, AUTOPLAY_MS);
                        }

                        function stopAutoplay() {
                            if (timer) {
                                clearInterval(timer);
                                timer = null;
                            }
                        }

                        // initial build
                        function init() {
                            // ensure only original slides counted
                            slideCount = Array.from(track.children).filter(n => !n.classList.contains('clone')).length;
                            slidesToShow = computeSlidesToShow();
                            buildClones();
                            setSizes();
                            // start at the first actual slide (accounting for prepended clones)
                            updatePosition(false);
                            startAutoplay();
                        }

                        // events
                        nextBtn && nextBtn.addEventListener('click', () => {
                            next();
                            startAutoplay();
                        });
                        prevBtn && prevBtn.addEventListener('click', () => {
                            prev();
                            startAutoplay();
                        });

                        container.addEventListener('mouseenter', stopAutoplay);
                        container.addEventListener('mouseleave', startAutoplay);

                        // keyboard: focus on container to use arrows
                        container.tabIndex = 0;
                        container.addEventListener('keydown', (e) => {
                            if (e.key === 'ArrowRight') {
                                next();
                                startAutoplay();
                            }
                            if (e.key === 'ArrowLeft') {
                                prev();
                                startAutoplay();
                            }
                        });

                        // responsive: re-init on resize
                        let rT = null;
                        window.addEventListener('resize', () => {
                            clearTimeout(rT);
                            rT = setTimeout(() => {
                                slidesToShow = computeSlidesToShow();
                                init();
                            }, 120);
                        });

                        // trigger init
                        setTimeout(init, 50);
                    }

                    // initialize all sliders with the class
                    document.addEventListener('DOMContentLoaded', function() {
                        const sliders = document.querySelectorAll('.crm-client-testimonisl-slider');
                        sliders.forEach(sl => initSlider(sl));
                    });
                })();
            </script>
