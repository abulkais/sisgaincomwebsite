    <script>
        (() => {
            const slider = document.getElementById('testimonialSlider');
            const viewport = slider.querySelector('.ct-viewport');
            const track = slider.querySelector('.ct-track');
            const prevBtn = slider.querySelector('.ct-prev');
            const nextBtn = slider.querySelector('.ct-next');

            let originals = Array.from(track.children);
            const CLONE_COUNT = 3;

            let index = 0;
            let step = 0;
            let perView = 3;
            let autoplayTimer = null;
            const AUTOPLAY_MS = 3000;
            let isAnimating = false;

            // ✅ RESPONSIVE COUNT
            function getPerView() {
                const w = window.innerWidth;
                if (w <= 640) return 1;
                if (w <= 1024) return 2;
                return 3;
            }

            // ✅ BUILD CLONES (FIXED)
            function buildClones() {
                track.innerHTML = "";

                const first = originals.slice(0, CLONE_COUNT).map(n => n.cloneNode(true));
                const last = originals.slice(-CLONE_COUNT).map(n => n.cloneNode(true));

                last.forEach(n => track.appendChild(n));
                originals.forEach(n => track.appendChild(n));
                first.forEach(n => track.appendChild(n));

                index = CLONE_COUNT;
            }

            // ✅ CALCULATE WIDTH PROPERLY
            function measure() {
                perView = getPerView();
                document.documentElement.style.setProperty('--ct-per-view', perView);

                const firstCard = track.querySelector('.ct-card');
                const gap = parseFloat(getComputedStyle(track).gap) || 0;

                step = firstCard.offsetWidth + gap;

                snap(false);
            }

            // ✅ MOVE SLIDER
            function snap(animate = true) {
                track.style.transition = animate ? 'transform 0.5s ease' : 'none';
                track.style.transform = `translateX(-${index * step}px)`;
            }

            // ✅ NEXT
            function goNext() {
                if (isAnimating) return;
                isAnimating = true;

                index++;
                snap(true);

                setTimeout(normalize, 500); // FIXED
            }

            // ✅ PREV
            function goPrev() {
                if (isAnimating) return;
                isAnimating = true;

                index--;
                snap(true);

                setTimeout(normalize, 500); // FIXED
            }

            // ✅ LOOP FIX
            function normalize() {
                const originalCount = originals.length;

                if (index >= originalCount + CLONE_COUNT) {
                    index = CLONE_COUNT;
                    snap(false);
                }

                if (index < CLONE_COUNT) {
                    index = originalCount + CLONE_COUNT - 1;
                    snap(false);
                }

                isAnimating = false;
            }

            // ✅ AUTOPLAY
            function startAutoplay() {
                stopAutoplay();
                autoplayTimer = setInterval(goNext, AUTOPLAY_MS);
            }

            function stopAutoplay() {
                if (autoplayTimer) clearInterval(autoplayTimer);
            }

            // ✅ EVENTS
            nextBtn.addEventListener('click', goNext);
            prevBtn.addEventListener('click', goPrev);

            slider.addEventListener('mouseenter', stopAutoplay);
            slider.addEventListener('mouseleave', startAutoplay);

            window.addEventListener('resize', () => {
                buildClones();
                setTimeout(measure, 100);
            });

            // ✅ INIT (IMPORTANT FIX)
            window.addEventListener('load', () => {
                originals = Array.from(track.children);
                buildClones();

                setTimeout(() => {
                    measure();
                    startAutoplay();
                }, 200);
            });
        })();
    </script>


