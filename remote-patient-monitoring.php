        let currentIndex = 0;

        function updateSlide(newIndex) {
            slides[currentIndex].classList.remove('active');
            currentIndex = newIndex;
            slides[currentIndex].classList.add('active');

            // disable prev/next at ends
            if (currentIndex === 0) {
                prevBtnslider.classList.add('disabled');
            } else {
                prevBtnslider.classList.remove('disabled');
            }
            if (currentIndex === slides.length - 1) {
                nextBtnslider.classList.add('disabled');
            } else {
                nextBtnslider.classList.remove('disabled');
            }
        }

        prevBtnslider.addEventListener('click', () => {
            if (currentIndex > 0) {
                updateSlide(currentIndex - 1);
            }
        });

        nextBtnslider.addEventListener('click', () => {
            if (currentIndex < slides.length - 1) {
                updateSlide(currentIndex + 1);
            }
        });

        // init state
        updateSlide(0);
    </script>


