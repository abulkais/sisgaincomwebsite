        // ==== Counter: increments on first scroll into view ====
        (function() {
            var counters = [].slice.call(document.querySelectorAll('.js-counter'));
            var started = false;

            function format(n, fmt) {
                if (fmt === 'comma') return n.toLocaleString('en-US');
                return n;
            }

            function animate(el) {
                var target = parseInt(el.getAttribute('data-target'), 10) || 0;
                var dur = parseInt(el.getAttribute('data-duration'), 10) || 1000;
                var fmt = el.getAttribute('data-format') || '';
                var start = 0,
                    startTime = null;

                function step(ts) {
                    if (!startTime) startTime = ts;
                    var p = Math.min((ts - startTime) / dur, 1);
                    var eased = 1 - Math.pow(1 - p, 3); // easeOutCubic
                    var val = Math.round(eased * target);
                    el.textContent = format(val, fmt);
                    if (p < 1) requestAnimationFrame(step);
                }
                requestAnimationFrame(step);
            }

            function onEnter() {
                if (started) return;
                started = true;
                counters.forEach(animate);
            }

            if ('IntersectionObserver' in window) {
                var io = new IntersectionObserver(function(entries) {
                    entries.forEach(function(e) {
                        if (e.isIntersecting) {
                            onEnter();
                            io.disconnect();
                        }
                    });
                }, {
                    threshold: 0.2
                });
                io.observe(document.querySelector('.excellence-stats'));
            } else {
                // Fallback: start when user scrolls near section
                function fallback() {
                    var rect = document.querySelector('.excellence-stats').getBoundingClientRect();
                    if (rect.top < window.innerHeight * 0.8) {
                        onEnter();
                        window.removeEventListener('scroll', fallback);
                    }
                }
                window.addEventListener('scroll', fallback, {
                    passive: true
                });
                fallback();
            }
        })();
    </script>
