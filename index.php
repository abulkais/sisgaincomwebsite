        document.addEventListener("DOMContentLoaded", () => {
            const serviceItems = Array.from(document.querySelectorAll('.solution-list__item'));
            const panels = Array.from(document.querySelectorAll('.panel.panel--solutions'));
            const mobileSelect = document.getElementById('mobile-service-select');

            // Map serviceId <-> elements
