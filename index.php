    <script>
        (function() {
            function getVisible() {
                const w = window.innerWidth;
                if (w < 576) return 1;
                if (w < 768) return 2;
                if (w < 992) return 3;
                return 3;
            }

            function getGap() {
                return parseFloat(getComputedStyle(document.documentElement).getPropertyValue('--trip-gap')) || 28;
            }

            function createCarousel(instance) {
                if (instance.__tripInit) return instance.__tripAPI; // don't double-init

                const viewport = instance.querySelector('.trip-carousel__viewport');
                const track = instance.querySelector('.trip-carousel__track');
                const prevBtn = instance.querySelector('.trip-btn--prev');
                const nextBtn = instance.querySelector('.trip-btn--next');
                const dotsWrap = instance.querySelector('.trip-dots');

                let slides = Array.from(track.children);
                const originalCount = slides.length;
                let index = 0;
                let visible = getVisible();
                let stepPx = 0;
                let autoplayTimer = null;
                let isTransitioning = false;

                function setVisibleVar() {
                    document.documentElement.style.setProperty('--trip-visible', String(visible));
                }

                function calcStep() {
                    const s = track.children[index] || track.children[0];
                    if (!s) return 0;
                    const rect = s.getBoundingClientRect();
                    return Math.round(rect.width + getGap());
                }

                function buildDots() {
                    dotsWrap.innerHTML = '';
                    for (let i = 0; i < originalCount; i++) {
                        const d = document.createElement('span');
                        d.className = 'trip-dot' + (i === 0 ? ' is-active' : '');
                        dotsWrap.appendChild(d);
                    }
                }

                function updateDots() {
                    const activeReal = (index - visible + originalCount) % originalCount;
                    Array.from(dotsWrap.children).forEach((d, i) => d.classList.toggle('is-active', i === activeReal));
                }

                function makeClones() {
                    const before = slides.slice(-visible).map(el => el.cloneNode(true));
                    const after = slides.slice(0, visible).map(el => el.cloneNode(true));
                    before.forEach(cl => track.insertBefore(cl, track.firstChild));
                    after.forEach(cl => track.appendChild(cl));
                }

                function setTranslate(noAnim = false) {
                    if (noAnim) track.style.transition = 'none';
                    const x = -index * stepPx;
                    track.style.transform = `translateX(${x}px)`;
                    if (noAnim) requestAnimationFrame(() => track.style.transition = 'transform .45s ease');
                }

                function normalizeIndex() {
                    if (index >= visible + originalCount) {
                        index = visible;
                        setTranslate(true);
                    } else if (index < visible) {
                        index = visible + originalCount - 1;
                        setTranslate(true);
                    }
                    updateDots();
                }

                function next() {
                    if (isTransitioning) return;
                    isTransitioning = true;
                    index += 1;
                    setTranslate();
                }

                function prev() {
                    if (isTransitioning) return;
                    isTransitioning = true;
                    index -= 1;
                    setTranslate();
                }

                function startAutoplay() {
                    stopAutoplay();
                    autoplayTimer = setInterval(next, 2800);
                }

                function stopAutoplay() {
                    if (autoplayTimer) {
                        clearInterval(autoplayTimer);
                        autoplayTimer = null;
                    }
                }

                function rebuild() {
                    stopAutoplay();
                    track.style.transition = 'none';
                    track.style.transform = 'translateX(0)';
                    // reset to originals
                    track.innerHTML = '';
                    slides.forEach(s => track.appendChild(s));

                    visible = getVisible();
                    setVisibleVar();
                    makeClones();
                    index = visible;

                    requestAnimationFrame(() => {
                        stepPx = calcStep();
                        setTranslate(true);
                        updateDots();
                        startAutoplay();
                    });
                }

                // listeners
                track.addEventListener('transitionend', () => {
                    isTransitioning = false;
                    normalizeIndex();
                });
                nextBtn.addEventListener('click', next);
                prevBtn.addEventListener('click', prev);
                viewport.addEventListener('mouseenter', stopAutoplay);
                viewport.addEventListener('mouseleave', startAutoplay);

                // init
                buildDots();
                rebuild();

                // public API for tab switching
                const api = {
                    rebuild,
                    startAutoplay,
                    stopAutoplay
                };
                instance.__tripInit = true;
                instance.__tripAPI = api;
                return api;
            }

            // Setup tabs
            const tabBtns = Array.from(document.querySelectorAll('.trip-tab'));
            const instances = Array.from(document.querySelectorAll('.trip-carousel-instance'));

            // init only the visible one first
            const initial = document.querySelector('.trip-carousel-instance:not(.is-hidden)');
            let currentAPI = createCarousel(initial);

            tabBtns.forEach(btn => {
                btn.addEventListener('click', () => {
                    if (btn.classList.contains('active')) return;

                    // toggle active tab UI
                    tabBtns.forEach(b => b.classList.remove('active'));
                    btn.classList.add('active');

                    const targetId = btn.getAttribute('data-trip-tab');
                    instances.forEach(inst => {
                        const match = inst.getAttribute('data-trip-id') === targetId;
                        inst.classList.toggle('is-hidden', !match);
                        if (match) {
                            const api = createCarousel(inst); // init if not already
                            api.rebuild();
                            api.startAutoplay();
                            currentAPI = api;
                        } else if (inst.__tripAPI) {
                            inst.__tripAPI.stopAutoplay();
                        }
                    });
                });
            });

            // Rebuild on resize (for the currently visible one)
            window.addEventListener('resize', () => {
                if (currentAPI) currentAPI.rebuild();
            });
        })();
