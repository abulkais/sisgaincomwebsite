            serviceItems.forEach(el => itemById[el.getAttribute('data-service')] = el);

            const panelById = {};
            panels.forEach(p => {
                const id = p.id.replace('solution', '');
                panelById[id] = p;
            });

            const scrollOffset = 120; // adjust if your fixed header height changes

            function smoothScrollToPanel(id) {
                const panel = panelById[id];
                if (!panel) return;
                const top = panel.getBoundingClientRect().top + window.pageYOffset - scrollOffset;
                window.scrollTo({
                    top,
                    behavior: 'smooth'
                });
            }

            function setActive(id) {
                // Sidebar items
                serviceItems.forEach(el => el.classList.toggle('active', el.getAttribute('data-service') === id));
                // Panels highlight
                panels.forEach(p => p.classList.toggle('highlighted', p.id === `solution${id}`));
                // Mobile select
                if (mobileSelect) mobileSelect.value = `solution${id}`;
            }

            // Click on sidebar items
            serviceItems.forEach(item => {
                item.addEventListener('click', (e) => {
                    e.preventDefault();
                    const id = item.getAttribute('data-service');
                    setActive(id);
                    smoothScrollToPanel(id);
                });
            });

            // Mobile select change
            if (mobileSelect) {
                mobileSelect.addEventListener('change', () => {
                    const value = mobileSelect.value; // e.g., "solution3"
                    const id = value.replace('solution', '');
                    setActive(id);
                    smoothScrollToPanel(id);
                });
            }

            // Track which section is most visible
            const ratios = {};
            const io = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    const id = entry.target.id.replace('solution', '');
                    ratios[id] = entry.intersectionRatio;
                });

                // Pick the panel with highest visibility on screen
                let bestId = null,
                    bestRatio = 0;
                Object.keys(ratios).forEach(id => {
                    if (ratios[id] > bestRatio) {
                        bestRatio = ratios[id];
                        bestId = id;
                    }
                });

                // Only update if something is actually visible
                if (bestId) setActive(bestId);
            }, {
                root: null,
                rootMargin: `-${scrollOffset}px 0px 0px 0px`,
                threshold: [0, 0.1, 0.25, 0.5, 0.75, 1]
            });

            panels.forEach(p => io.observe(p));

            // Initial state: choose hash if present, else first panel
            const hash = window.location.hash && window.location.hash.startsWith('#solution') ?
                window.location.hash.replace('#solution', '') :
                null;

            const initialId = hash || (panels[0] ? panels[0].id.replace('solution', '') : '1');
            setActive(initialId);
        });
