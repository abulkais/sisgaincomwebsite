    <script>
        (() => {
            const slider = document.getElementById('testimonialSlider');
            const viewport = slider.querySelector('.ct-viewport');
            const track = slider.querySelector('.ct-track');
            const prevBtn = slider.querySelector('.ct-prev');
            const nextBtn = slider.querySelector('.ct-next');

            let originals = Array.from(track.children); // original slides
            const CLONE_COUNT = 3; // to support 3-per-view
            let index = 0; // current pointer in ALL (with clones)
            let perView = getPerView();
            let step = 0; // px to move per 1 slide
            let gapPx = 0;
            let autoplayTimer = null;
            const AUTOPLAY_MS = 3500;
            let isAnimating = false;

            // --- build clones for seamless loop ---
            function buildClones() {
                // Remove any existing clones (if resizing rebuilds)
                Array.from(track.children).forEach(el => {
                    if (el.dataset.clone === 'true') el.remove();
                });
                // Add head/tail clones
                const first = originals.slice(0, CLONE_COUNT).map(n => n.cloneNode(true));
                const last = originals.slice(-CLONE_COUNT).map(n => n.cloneNode(true));

                first.forEach(n => {
                    n.dataset.clone = 'true';
                    track.appendChild(n);
                });
                last.forEach(n => {
                    n.dataset.clone = 'true';
                    track.insertBefore(n, track.firstChild);
                });

                // Start at first original (after leading clones)
                index = CLONE_COUNT;
                snap(false);
            }

            // --- measure geometry ---
            function measure() {
                // update perView based on width
                perView = getPerView();
                document.documentElement.style.setProperty('--ct-per-view', perView);

                // read gap and card width to calculate step
                const gs = getComputedStyle(track);
                gapPx = parseFloat(gs.columnGap || gs.gap) || 0;
                const firstCard = track.querySelector('.ct-card');
                const cardWidth = firstCard.getBoundingClientRect().width;
                step = cardWidth + gapPx;

                snap(false);
            }

            function getPerView() {
                const w = window.innerWidth;
                if (w <= 640) return 1;
                if (w <= 1024) return 2;
                return 3;
            }

            function snap(withTransition = true) {
                if (withTransition) {
                    track.style.transition = 'transform 480ms cubic-bezier(.22,.61,.36,1)';
                } else {
                    track.style.transition = 'none';
                }
                const x = -index * step;
                track.style.transform = `translateX(${x}px)`;
            }

            function goBy(delta) {
                if (isAnimating) return;
                isAnimating = true;
                index += delta;
                snap(true);
            }

            function normalizeAfterTransition() {
                const total = track.children.length;
                const originalCount = originals.length;
                const lowerBound = CLONE_COUNT;
                const upperBound = CLONE_COUNT + originalCount - 1;

                if (index > upperBound) {
                    index -= originalCount;
                    snap(false);
                }
                if (index < lowerBound) {
                    index += originalCount;
                    snap(false);
                }
                // allow next animation frame to unlock
                setTimeout(() => {
                    isAnimating = false;
                }, 20);
            }

            // --- autoplay ---
            function startAutoplay() {
                stopAutoplay();
                autoplayTimer = setInterval(() => goBy(1), AUTOPLAY_MS);
            }

            function stopAutoplay() {
                if (autoplayTimer) clearInterval(autoplayTimer);
                autoplayTimer = null;
            }

            // --- drag support (mouse & touch via Pointer Events) ---
            let drag = {
                active: false,
                startX: 0,
                startT: 0,
                lastDX: 0
            };

            function onPointerDown(e) {
                e.preventDefault();
                stopAutoplay();
                drag.active = true;
                track.classList.add('is-dragging');
                drag.startX = e.clientX;
                drag.startT = performance.now();
                const matrix = new DOMMatrixReadOnly(getComputedStyle(track).transform);
                drag.base = matrix.m41; // current translateX
                drag.lastDX = 0;
                track.setPointerCapture(e.pointerId);
            }

            function onPointerMove(e) {
                if (!drag.active) return;
                const dx = e.clientX - drag.startX;
                drag.lastDX = dx;
                track.style.transition = 'none';
                track.style.transform = `translateX(${drag.base + dx}px)`;
            }

            function onPointerUp(e) {
                if (!drag.active) return;
                drag.active = false;
                track.classList.remove('is-dragging');
                const threshold = step * 0.25;
                let moved = 0;
                if (Math.abs(drag.lastDX) > threshold) {
                    moved = Math.round(drag.lastDX / step);
                }
                // negative dx -> move next (+1)
                if (moved < 0) {
                    goBy(1);
                } else if (moved > 0) {
                    goBy(-1);
                } else {
                    snap(true);
                }

                track.releasePointerCapture(e.pointerId);
                startAutoplay();
            }

            // --- events ---
            nextBtn.addEventListener('click', () => goBy(1));
            prevBtn.addEventListener('click', () => goBy(-1));
            track.addEventListener('transitionend', normalizeAfterTransition);
            viewport.addEventListener('pointerdown', onPointerDown);
            viewport.addEventListener('pointermove', onPointerMove);
            viewport.addEventListener('pointerup', onPointerUp);
            viewport.addEventListener('pointercancel', onPointerUp);
            viewport.addEventListener('mouseleave', e => {
                if (drag.active) onPointerUp(e);
            });

            slider.addEventListener('mouseenter', stopAutoplay);
            slider.addEventListener('mouseleave', startAutoplay);
            window.addEventListener('resize', () => {
                measure();
            });

            // --- init ---
            buildClones();
            // Ensure measure after images load for correct widths
            window.addEventListener('load', () => {
                measure();
                startAutoplay();
            });
        })();
    </script>
